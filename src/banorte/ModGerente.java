    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            RandomAccessFile miArchivo=new RandomAccessFile("infoGerente.dat","rw");
            nombre=jTextField3.getText();
            if(nombre.length()<25){////En caso de que el dato ingresado sea menor se llena la variable de espacios.
                for(int i=nombre.length(); i<25; i++)
                    nombre=nombre+" ";
            }
            else
                nombre=nombre.substring(0,25);
            ID="1"+jTextField2.getText();
            id=Integer.parseInt(ID);
            departamento=jTextField4.getText();
            if(departamento.length()<20){////En caso de que el dato ingresado sea menor se llena la variable de espacios.
                for(int i=departamento.length(); i<20; i++)
                    departamento+=" ";
            }
            else
                departamento=departamento.substring(0,20);
            if(ID.length()==9){
                miArchivo.seek(contador*tamRegistro);
                miArchivo.writeInt(id);
                miArchivo.writeChars(nombre);
                miArchivo.writeChars(departamento);
                GerenteModificado m=new GerenteModificado();
                m.setLocationRelativeTo(null);
                m.setVisible(true);
            }
            else{
                ErrorModGer m=new ErrorModGer();
                m.setLocationRelativeTo(null);
                m.setVisible(true);
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(ModGerente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ModGerente.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nombre="", departamento="", pruebaId2="", linea="", ID="";
        int tamRegistro=94, numRegistros,actual,identificador=0,contador=0,pruebaId, id=0;
        boolean encontrado=false;           
        ID="1"+jTextField1.getText();
        try {
            RandomAccessFile miArchivo=new RandomAccessFile("infoGerente.dat","rw");
            numRegistros=(int)(miArchivo.length()/tamRegistro);
            while((contador<numRegistros)&&(encontrado==false)){
                miArchivo.seek(contador*tamRegistro);
                pruebaId=miArchivo.readInt();
                id=Integer.parseInt(ID);
                if(pruebaId==id){
                    miArchivo.seek(contador*tamRegistro +4);
                    for(int i=0; i<25; i++)
                        nombre=nombre+miArchivo.readChar();
                    for(int j=0; j<20; j++)
                        departamento=departamento+miArchivo.readChar();
                    linea="Nombre: "+nombre+"\nDepartamento: "+departamento+"\nID: "+ID.substring(1,9);
                    encontrado=true;
                }
                else
                    contador++;
            }
            if(!encontrado){
                textArea1.setText("Ese ID no corresponde a ningun gerente registrado o no lo introdujo correctamente.\nFavor de verificar.");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
            }
            else{
                jTextField2.setText(ID.substring(1,9).trim());
                jTextField3.setText(nombre.trim());
                jTextField4.setText(departamento.trim());
                textArea1.setText(linea.trim());
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MostrarInfoGer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(MostrarInfoGer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

